    def __init__(self, parent):
        super().__init__()
        self.set_title("Справка")
        self.set_search_enabled(False)

        for pd in self._PAGES:
            page = Adw.PreferencesPage()
            page.set_title(pd["title"])
            page.set_icon_name(pd["icon"])
            self.add(page)

            for gd in pd["groups"]:
                grp = Adw.PreferencesGroup()
                grp.set_title(gd["title"])
                if gd.get("description"):
                    grp.set_description(gd["description"])
                page.add(grp)

                for item in gd.get("items", []):
                    row = Adw.ActionRow()
                    row.set_title(item[0])
                    row.set_subtitle(item[1])
                    row.set_subtitle_selectable(False)
                    grp.add(row)

        self.present(parent)
